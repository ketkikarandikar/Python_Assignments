# How Do You Check The Presence Of A Key In A Dictionary?

# Initialize the dictionary
dict = {1: "Ketki", 2: "Karandikar", 3: "Neel", 4: "Golwelkar"}

# Key to check

check = 2

# Check if the key exists in the dictionary

if check in dict:
    print("The key '{check}' exists in the dictionary.")      # if the key exists in the dictionary
else:
    print("The key '{check}' does not exists in the dictionary.")   # if the key does not exists in the dictionary
    

