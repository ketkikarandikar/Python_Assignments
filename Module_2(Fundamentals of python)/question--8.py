# Write a python program to test whether a passed letter is a vowel or not ?


# Check whether a character is vowel or consonant 

K = (input("Enter letter : "))
if (K == 'a' or K == 'e' or K == 'i' or K == 'o' or K == 'u'):   # vowels
  print("Vowel")

elif (K == 'A' or K == 'E' or K == 'I' or K == 'O' or K == 'U'):   # vowels
  print("vowel")

else:
  print("Consonant") 
